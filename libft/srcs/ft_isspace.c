#include "libft.h"

int	ft_isspace(int c)
{
	if (c == ' ' || c == '\t' || c == '\n' || c == '\v' || c == '\f')
	{
		return (SPACE);
	}
	else
	{
		return (NOT_SPACE);
	}
}
